package arreglos.ejercicio7;

public class Nota {

    private int valor;

    public Nota(int valor) {
        this.valor = valor;
    }

    public Nota() {
    }

    /**
     * @return the valor
     */
    public int getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(int valor) {
        this.valor = valor;
    }
}
